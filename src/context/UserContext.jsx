import { createContext, useState, useEffect } from "react";
import { validateTokenOnPageLoad, ensureTokenConsistency } from "../services/axiosClient";

// 1. ƒê·ªãnh nghƒ©a c√°c vai tr√≤ v√† quy·ªÅn
export const USER_ROLES = {
  ADMIN: "ADMIN",
  MANAGER: "MANAGER",
  DOCTOR: "DOCTOR",
  PATIENT: "PATIENT",
  CUSTOMER: "CUSTOMER",
};

// Map backend roles to frontend roles
const ROLE_MAPPING = {
  // Standard uppercase (backend preference)
  ADMIN: USER_ROLES.ADMIN,
  MANAGER: USER_ROLES.MANAGER,
  DOCTOR: USER_ROLES.DOCTOR,
  PATIENT: USER_ROLES.PATIENT,
  CUSTOMER: USER_ROLES.CUSTOMER,

  // Capitalized (actual backend format from SQL)
  Admin: USER_ROLES.ADMIN,
  Manager: USER_ROLES.MANAGER,
  Doctor: USER_ROLES.DOCTOR,
  Patient: USER_ROLES.PATIENT,
  Customer: USER_ROLES.CUSTOMER,

  // Legacy lowercase support
  admin: USER_ROLES.ADMIN,
  manager: USER_ROLES.MANAGER,
  doctor: USER_ROLES.DOCTOR,
  patient: USER_ROLES.PATIENT,
  customer: USER_ROLES.CUSTOMER,

  // Additional doctor role variations
  physician: USER_ROLES.DOCTOR,
  medical_doctor: USER_ROLES.DOCTOR,
  doc: USER_ROLES.DOCTOR,
  PHYSICIAN: USER_ROLES.DOCTOR,
  MEDICAL_DOCTOR: USER_ROLES.DOCTOR,
  DOC: USER_ROLES.DOCTOR,
};

export const ROLE_PERMISSIONS = {
  [USER_ROLES.ADMIN]: {
    canManageUsers: true,
    canManageDepartments: true,
    canViewReports: true,
    canManageSystem: true,
    canAccessAll: true,
  },
  [USER_ROLES.MANAGER]: {
    canManageDoctors: true,
    canManageSchedule: true,
    canViewTeamReports: true,
    canManageTeam: true,
  },
  [USER_ROLES.DOCTOR]: {
    canManagePatients: true,
    canCreateTreatmentPlan: true,
    canViewOwnSchedule: true,
    canUpdateTreatmentStatus: true,
  },
  [USER_ROLES.PATIENT]: {
    canViewTreatmentProcess: true,
    canViewSchedule: true,
    canViewNotifications: true,
    canViewProfile: true,
  },
  [USER_ROLES.CUSTOMER]: {
    canViewPublicContent: true,
    canRegisterService: true,
    canViewProfile: true,
  },
};

// 2. T·∫°o Context
export const UserContext = createContext();

// 3. UserProvider k·∫øt h·ª£p
export const UserProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [isUserLoading, setIsUserLoading] = useState(true);

  // Khi load l·∫°i trang, l·∫•y user t·ª´ localStorage (c√≥ x·ª≠ l√Ω l·ªói)
  useEffect(() => {
    const loadUserFromStorage = () => {
      // üÜï S·ª≠ d·ª•ng function validation ƒë·ªÉ ki·ªÉm tra token
      const isValidToken = validateTokenOnPageLoad();
      
      if (!isValidToken) {
        console.log("‚ÑπÔ∏è [UserContext] Token validation failed, user not logged in");
        setUser(null);
        setIsLoggedIn(false);
        return;
      }
      
      // üÜï ƒê·∫£m b·∫£o token consistency tr∆∞·ªõc khi load user
      ensureTokenConsistency();
      
      const storedUser = localStorage.getItem("user");
      const storedToken = localStorage.getItem("token") || localStorage.getItem("accessToken");
      
      try {
        if (storedUser && storedToken) {
          const userData = JSON.parse(storedUser);
          console.log(
            "üîÑ [UserContext] Loading user from localStorage:",
            userData
          );
          
          // üÜï Ki·ªÉm tra xem token c√≥ h·ª£p l·ªá kh√¥ng
          if (userData.token && userData.token === storedToken) {
            setUser(userData);
            setIsLoggedIn(true);
            console.log("‚úÖ [UserContext] User loaded successfully from localStorage");
          } else {
            console.warn("‚ö†Ô∏è [UserContext] Token mismatch, clearing invalid data");
            localStorage.removeItem("user");
            localStorage.removeItem("token");
            localStorage.removeItem("accessToken");
            setUser(null);
            setIsLoggedIn(false);
          }
        } else if (storedUser && !storedToken) {
          // C√≥ user data nh∆∞ng kh√¥ng c√≥ token ri√™ng bi·ªát
          const userData = JSON.parse(storedUser);
          if (userData.token) {
            // Sync token t·ª´ user data v√†o localStorage
            localStorage.setItem("token", userData.token);
            localStorage.setItem("accessToken", userData.token);
            setUser(userData);
            setIsLoggedIn(true);
            console.log("‚úÖ [UserContext] User loaded and token synced from user data");
          } else {
            console.warn("‚ö†Ô∏è [UserContext] User data has no token, clearing");
            localStorage.removeItem("user");
            setUser(null);
            setIsLoggedIn(false);
          }
        } else {
          console.log("‚ÑπÔ∏è [UserContext] No user data found in localStorage");
          setUser(null);
          setIsLoggedIn(false);
        }
      } catch (error) {
        console.error("‚ùå L·ªói parse user t·ª´ localStorage:", error);
        // üÜï Ch·ªâ clear user-related data, kh√¥ng clear treatment data
        localStorage.removeItem("user");
        localStorage.removeItem("token");
        localStorage.removeItem("accessToken");
        setUser(null);
        setIsLoggedIn(false);
      }
    };
    
    // üÜï Th√™m delay nh·ªè ƒë·ªÉ ƒë·∫£m b·∫£o localStorage ƒë√£ s·∫µn s√†ng
    const timer = setTimeout(loadUserFromStorage, 100);
    
    return () => clearTimeout(timer);
  }, []);
  
  // üÜï Th√™m useEffect ƒë·ªÉ set loading state
  useEffect(() => {
    const timer = setTimeout(() => {
      setIsUserLoading(false);
    }, 200);
    
    return () => clearTimeout(timer);
  }, []);

  // üÜï Th√™m useEffect ƒë·ªÉ ƒë·ªãnh k·ª≥ ki·ªÉm tra token consistency
  useEffect(() => {
    if (!isLoggedIn || !user) return;
    
    const checkTokenConsistency = () => {
      ensureTokenConsistency();
    };
    
    // Ki·ªÉm tra m·ªói 30 gi√¢y
    const interval = setInterval(checkTokenConsistency, 30000);
    
    return () => clearInterval(interval);
  }, [isLoggedIn, user]);

  // ƒêƒÉng nh·∫≠p (c√≥ role & tr·∫°ng th√°i d·ªãch v·ª• m·∫∑c ƒë·ªãnh)
  const login = async (userData) => {
    // Ch·ªâ log khi debug mode
    if (process.env.NODE_ENV === 'development' && false) { // T·∫Øt log
      console.log("üîç [UserContext] Login data received:", userData);
      console.log("üîç [UserContext] Raw role from backend:", userData.role);
    }

    // ü©∫ QUICK FIX: Auto-detect doctor role for test accounts
    let finalRole = userData.role;
    if (!finalRole && userData.email) {
      const doctorEmails = [
        "doctor.test@ferticare.com",
        "doctor.ivf@ferticare.com",
        "doctor.iui@ferticare.com",
        "doctor.ob@ferticare.com",
      ];

      if (
        doctorEmails.includes(userData.email) ||
        userData.fullName?.includes("BS.") ||
        userData.fullName?.includes("Dr.") ||
        userData.email?.includes("doctor.")
      ) {
        finalRole = "DOCTOR";
        console.log(
          "ü©∫ [UserContext] QUICK FIX: Auto-detected doctor role for:",
          userData.email
        );
      }
    }

    // Map role from backend to frontend
    const mappedRole = ROLE_MAPPING[finalRole] || USER_ROLES.CUSTOMER;

    // Ch·ªâ log khi debug mode
    if (process.env.NODE_ENV === 'development' && false) { // T·∫Øt log
      console.log("üîç [UserContext] Mapped role:", finalRole, "=>", mappedRole);

      // Debug: Show available mappings if role not found
      if (!ROLE_MAPPING[finalRole]) {
        console.warn("‚ö†Ô∏è [UserContext] Role not found in mapping:", finalRole);
        console.warn("Available mappings:", Object.keys(ROLE_MAPPING));
      }
    }

    const dataToStore = {
      ...userData,
      role: mappedRole,
      token: userData.token,
      hasRegisteredService: userData.hasRegisteredService || false,
    };

    console.log("üîç [UserContext] Data to store:", dataToStore);
    console.log("üîç [UserContext] Token exists:", !!dataToStore.token);
    console.log("üîç [UserContext] Token preview:", dataToStore.token ? dataToStore.token.substring(0, 50) + "..." : "NO TOKEN");
    console.log(
      "üîç [UserContext] Dashboard path will be:",
      getDashboardPathForRole(mappedRole)
    );

    setUser(dataToStore);
    setIsLoggedIn(true);

    // ‚úÖ L∆∞u c·∫£ user object v√† token ri√™ng bi·ªát ƒë·ªÉ ƒë·∫£m b·∫£o compatibility
    localStorage.setItem("user", JSON.stringify(dataToStore));
    if (userData.token) {
      localStorage.setItem("token", userData.token);
      localStorage.setItem("accessToken", userData.token); // Also save to STORAGE_KEYS format
      console.log("‚úÖ [UserContext] Token saved to localStorage (both formats)");
    } else {
      console.warn("‚ö†Ô∏è [UserContext] No token found in userData:", userData);
    }

    // üîÑ Fetch th√™m profile data ƒë·ªÉ l·∫•y avatar m·ªõi nh·∫•t (skip mock tokens v√† l·ªói)
    if (!userData.token?.includes("mock") && !userData.token?.includes("error")) {
      try {
        // Ch·ªâ log khi debug mode
        if (process.env.NODE_ENV === 'development' && false) {
          console.log("üîÑ [UserContext] Fetching fresh profile data for avatar...");
        }

        // Dynamic import ƒë·ªÉ tr√°nh circular dependency
        const { default: apiProfile } = await import("../api/apiProfile");

        let profileData;
        try {
          switch (mappedRole.toUpperCase()) {
            case USER_ROLES.DOCTOR:
              profileData = await apiProfile.getDoctorProfile();
              break;
            case USER_ROLES.MANAGER:
            case USER_ROLES.ADMIN:
              profileData = await apiProfile.getManagerAdminProfile();
              break;
            case USER_ROLES.CUSTOMER:
            case USER_ROLES.PATIENT:
              profileData = await apiProfile.getCustomerProfile();
              break;
            default:
              profileData = await apiProfile.getMyProfile();
              break;
          }
        } catch (profileError) {
          // Ch·ªâ log khi debug mode
          if (process.env.NODE_ENV === 'development' && false) {
            console.warn("‚ö†Ô∏è [UserContext] Profile API failed:", profileError.message);
          }
          // B·ªè fallback ƒë·ªÉ tr√°nh l·ªói th√™m
          profileData = null;
        }

        // C·∫≠p nh·∫≠t user v·ªõi avatar m·ªõi nh·∫•t t·ª´ profile
        if (profileData?.avatarUrl) {
          const updatedUserData = {
            ...dataToStore,
            avatarUrl: profileData.avatarUrl,
          };

          console.log(
            "‚úÖ [UserContext] Updated user data with fresh avatar:",
            updatedUserData.avatarUrl
          );

          setUser(updatedUserData);
          localStorage.setItem("user", JSON.stringify(updatedUserData));
          
          // üîÑ ƒê·∫£m b·∫£o token v·∫´n ƒë∆∞·ª£c l∆∞u ri√™ng bi·ªát
          if (userData.token) {
            localStorage.setItem("token", userData.token);
            console.log("‚úÖ [UserContext] Token re-saved after profile update");
          }
        }
      } catch (error) {
        console.warn(
          "‚ö†Ô∏è [UserContext] Could not fetch profile for avatar:",
          error.message
        );
        // Kh√¥ng throw error ƒë·ªÉ kh√¥ng block login process
      }
    } else {
      console.log("üîÑ [UserContext] Skipping profile fetch for mock token");
    }
  };

  // ƒêƒÉng nh·∫≠p b·∫±ng Google
  const loginWithGoogle = async (googleUser) => {
    const userData = {
      fullName: googleUser.name,
      email: googleUser.email,
      role: USER_ROLES.CUSTOMER,
      hasRegisteredService: false,
    };
    await login(userData);
  };

  // ƒêƒÉng k√Ω d·ªãch v·ª•, chuy·ªÉn customer => patient
  const updateServiceRegistration = (registrationData) => {
    if (!user) return;
    const updatedUser = {
      ...user,
      role: USER_ROLES.PATIENT,
      hasRegisteredService: true,
      serviceInfo: registrationData,
    };
    setUser(updatedUser);
    localStorage.setItem("user", JSON.stringify(updatedUser));
    
    // üîÑ ƒê·∫£m b·∫£o token v·∫´n ƒë∆∞·ª£c l∆∞u ri√™ng bi·ªát
    if (user.token) {
      localStorage.setItem("token", user.token);
      localStorage.setItem("accessToken", user.token);
      console.log("‚úÖ [UserContext] Token re-saved after service registration update");
    }
  };

  // ƒêƒÉng xu·∫•t
  const logout = () => {
    console.log("üö™ [UserContext] Logging out user...");
    
    // üÜï Ch·ªâ clear authentication data, gi·ªØ l·∫°i treatment data
    localStorage.removeItem("user");
    localStorage.removeItem("token");
    localStorage.removeItem("accessToken");
    
    // üÜï Kh√¥ng clear treatment data ƒë·ªÉ user c√≥ th·ªÉ ti·∫øp t·ª•c khi login l·∫°i
    // localStorage.removeItem("treatmentFlow"); // Comment out ƒë·ªÉ gi·ªØ treatment data
    
    setUser(null);
    setIsLoggedIn(false);
    
    console.log("‚úÖ [UserContext] User logged out successfully");
  };

  // Force refresh user data t·ª´ server (d√πng khi c·∫ßn sync avatar m·ªõi)
  const refreshUserData = async () => {
    if (!user?.token) return;

    try {
      console.log("üîÑ [UserContext] Refreshing user data...");

      const { default: apiProfile } = await import("../api/apiProfile");

      let profileData;
      switch (user.role?.toUpperCase()) {
        case USER_ROLES.CUSTOMER:
        case USER_ROLES.PATIENT:
          profileData = await apiProfile.getCustomerProfile();
          break;
        case USER_ROLES.DOCTOR:
          profileData = await apiProfile.getDoctorProfile();
          break;
        case USER_ROLES.MANAGER:
        case USER_ROLES.ADMIN:
          profileData = await apiProfile.getManagerAdminProfile();
          break;
        default:
          profileData = await apiProfile.getMyProfile();
          break;
      }

      const updatedUser = {
        ...user,
        avatarUrl: profileData?.avatarUrl || user.avatarUrl,
      };

      setUser(updatedUser);
      localStorage.setItem("user", JSON.stringify(updatedUser));
      
      // üîÑ ƒê·∫£m b·∫£o token v·∫´n ƒë∆∞·ª£c l∆∞u ri√™ng bi·ªát
      if (user.token) {
        localStorage.setItem("token", user.token);
        localStorage.setItem("accessToken", user.token);
        console.log("‚úÖ [UserContext] Token re-saved after user data refresh");
      }

      console.log("‚úÖ [UserContext] User data refreshed successfully");
      return updatedUser;
    } catch (error) {
      console.warn(
        "‚ö†Ô∏è [UserContext] Failed to refresh user data:",
        error.message
      );
      return user;
    }
  };

  // Ki·ªÉm tra permission
  const hasPermission = (permission) => {
    if (!user || !user.role) return false;
    return ROLE_PERMISSIONS[user.role]?.[permission] || false;
  };

  // Ki·ªÉm tra vai tr√≤
  const hasRole = (role) => user?.role === role;

  // Helper function to get dashboard path for any role
  const getDashboardPathForRole = (role) => {
    if (!role) return "/";
    console.log("üîç [UserContext] getDashboardPathForRole input:", role);
    switch (role.toUpperCase()) {
      case USER_ROLES.ADMIN:
        return "/admin/dashboard";
      case USER_ROLES.MANAGER:
        return "/manager/dashboard";
      case USER_ROLES.DOCTOR:
        return "/doctor-dashboard";
      case USER_ROLES.PATIENT:
        return "/patient/dashboard";
      case USER_ROLES.CUSTOMER:
      default:
        return "/";
    }
  };

  // L·∫•y dashboard path
  const getDashboardPath = () => {
    if (!user?.role) return "/";
    console.log("üîç [UserContext] getDashboardPath for user role:", user.role);
    const path = getDashboardPathForRole(user.role);
    console.log("üîç [UserContext] Dashboard path resolved:", path);
    return path;
  };

  // C√≥ ƒë∆∞·ª£c truy c·∫≠p patient area kh√¥ng?
  const canAccessPatientArea = () =>
    user?.role === USER_ROLES.PATIENT && user?.hasRegisteredService;



  return (
    <UserContext.Provider
      value={{
        user,
        setUser,
        isLoggedIn,
        isUserLoading,
        login,
        loginWithGoogle,
        logout,
        updateServiceRegistration,
        hasPermission,
        hasRole,
        getDashboardPath,
        canAccessPatientArea,
        refreshUserData,
        USER_ROLES,
        ROLE_PERMISSIONS,
      }}
    >
      {children}
    </UserContext.Provider>
  );
};
